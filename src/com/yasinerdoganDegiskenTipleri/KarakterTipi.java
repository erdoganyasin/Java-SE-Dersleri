package com.yasinerdoganDegiskenTipleri;

public class KarakterTipi {

	public static void main(String[] args) {
		char karakter1 , karakter2;
		
		karakter1 = 65;
		karakter2 = 'F';
		System.out.println("1.ATAMALAR");
		System.out.println("KARAKTER 1 SONUC : "+ karakter1);
		System.out.println("KARAKTER 2 SONUC : "+ karakter2);
		
		
		
		karakter1 = (char) (karakter1 + 1);
		karakter2 = 'K';
		System.out.println("1.ATAMALAR");
		System.out.println("KARAKTER 1 SONUC : "+ karakter1);
		System.out.println("KARAKTER 2 SONUC : "+ karakter2);
		

	}

}
